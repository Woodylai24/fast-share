// Load release keystore properties from android/key.properties (not committed).
// See key.properties.example. Falls back to debug signing if absent so local
// builds keep working until the upload keystore is configured.
val keystoreProperties = Properties().apply {
    val keystorePropertiesFile = rootProject.file("key.properties")
    if (keystorePropertiesFile.exists()) {
        load(FileInputStream(keystorePropertiesFile))
    }
}

android {
    namespace = "io.github.woodylai24.fastshare"
    compileSdk = flutter.compileSdkVersion
    ndkVersion = flutter.ndkVersion

    compileOptions {
